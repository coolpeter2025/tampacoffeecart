// Generate service+location combinations for SEO pages
export function getServiceLocationPages() {
  const pages: Array<{
    serviceSlug: string;
    locationSlug: string;
    service: typeof services[0];
    location: typeof locations[0];
  }> = [];
  
  for (const service of services) {
    for (const location of locations) {
      pages.push({
        serviceSlug: service.slug,
        locationSlug: location.slug,
        service,
        location,
      });
    }
  }
  
  return pages;
}

// SEO helper functions
export function generateMetaTitle(service: string, location: string): string {
  return `${service} ${location} | Mobile Coffee Cart | Tampa Bay Coffee Cart`;
}

